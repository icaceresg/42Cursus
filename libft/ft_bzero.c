#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*a;
	size_t			i;

	i = 0;
	a = s;
	while (n > 0)
	{
		a[i] = '\0';
		n--;
		i++;
	}
}
